# 1. Переменная с числом
age = 18
print(age)

# 2. Переменная со строкой (текст)
name = "Аня"
print(name)

# 3. Переменная с результатом вычисления
a = 5
b = 3
summa = a + b
print(summa)

# 4. Переменная, значение которой меняется
counter = 0
counter = counter + 1
print(counter)

# 5. Переменная с логическим значением (True / False)
is_student = True
print(is_student)
