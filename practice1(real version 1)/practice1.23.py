# 1. int — целое число
age = 20
print(age)
print(type(age))

# 2. float — число с точкой
price = 9.99
print(price)
print(type(price))

# 3. str — строка (текст)
name = "Иван"
print(name)
print(type(name))

# 4. bool — логический тип (True / False)
is_sunny = True
print(is_sunny)
print(type(is_sunny))

# 5. list — список
numbers = [1, 2, 3]
print(numbers)
print(type(numbers))
